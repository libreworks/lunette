require_once dirname(dirname(dirname(__FILE__))) . DIRECTORY_SEPARATOR . 'TestHelper.php';
require_once 'Lunette/Version.php';

/**
 * Test class for Lunette_Version.
 * Generated by PHPUnit on 2008-05-28 at 19:08:37.
 */
class Lunette_VersionTest extends PHPUnit_Framework_TestCase
{
    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';
        $suite  = new PHPUnit_Framework_TestSuite('Lunette_VersionTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }
    
    /**
     * Tests the 'compareVersion' method
     */
    public function testCompareVersion()
    {
        $this->assertEquals(-1, Lunette_Version::compareVersion('0.0.1'));
        $this->assertEquals(0, Lunette_Version::compareVersion(Lunette_Version::VERSION));
        $this->assertEquals(1, Lunette_Version::compareVersion('20.0.0'));
    }
}

