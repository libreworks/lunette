require_once 'Xyster/Orm.php';

/**
 * Test class for Lunette_Package_Cached.
 * Generated by PHPUnit on 2008-06-16 at 15:37:16.
 */
class Lunette_Package_CachedTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var    Lunette_Package_Cached
     */
    protected $object;
    
    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';
        $suite  = new PHPUnit_Framework_TestSuite('Lunette_Package_CachedTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
        $setup = new Lunette_TestDbSetup;
        $setup->setupPackage();
        
        $package = new LunettePackage;
        $package->name = 'foobar-package';
        $package->version = '2.0.2-lunette5';
        $package->state = 5;
        $package->installedSize = 3123.58;
        $package->essential = 'yes';
        $package->depends = 'food, water, shelter (>= 2.0)';
        $package->provides = 'foo, bar';
        $package->conflicts = 'foo, bar';
        $package->preinst = 'echo "Hello, world!";';
        $package->files = "\n" . implode("\n", array('myfile.txt', 'myfile2.txt')) . "\n";
        $this->object = new Lunette_Package_Cached($package);
    }

    /**
     * Tears down the fixture
     */
    protected function tearDown()
    {
        $setup = new Lunette_TestDbSetup;
        $setup->tearDownPackage();
    }
    
    /**
     * Tests the 'getControlValue' method
     */
    public function testGetControlValue()
    {
        $values =  array(
                'essential' => 'yes',
                'package' => 'foobar-package',
                'version' => '2.0.2-lunette5',
                'installed-size' => 3123.58
            );
        foreach( $values as $key => $expected ) {
            $this->assertEquals($expected, $this->object->getControlValue($key));
        }
    }
    
    /**
     * Tests the 'getFiles' method
     */
    public function testGetFiles()
    {
        $this->assertEquals(array('myfile.txt', 'myfile2.txt'), $this->object->getFiles());
    }

    /**
     * Tests the 'getRelations' method
     */
    public function testGetRelations()
    {
        foreach( range(0, 6) as $value ) {
            $type = Xyster_Enum::valueOf('Lunette_Package_Relation_Type', $value);
            $relations = $this->object->getRelations($type);
            $this->assertType('Lunette_Package_Relation_Set', $relations);
            $this->assertSame($relations, $this->object->getRelations($type));
            if ( $type === Lunette_Package_Relation_Type::Depends() ) {
                $this->assertEquals(3, count($relations));
            } else if ( $type === Lunette_Package_Relation_Type::Conflicts() ) {
                $this->assertEquals(2, count($relations));
            } else if ( $type === Lunette_Package_Relation_Type::Provides() ) {
                $this->assertEquals(2, count($relations));
            }
        }
    }
    
    /**
     * Tests the 'getScript' method
     */
    public function testGetScript()
    {
        $actual = $this->object->getScript('preinst');
        $this->assertEquals('echo "Hello, world!";', $actual);
    }
    
    /**
     * Tests the 'getScript' method with a bad script name
     */
    public function testGetScriptInvalid()
    {
        $this->setExpectedException('Lunette_Package_Exception', 'Invalid script name: foobar');
        $this->object->getScript('foobar');
    }

    /**
     * Tests the 'getScriptRunner' method
     */
    public function testGetScriptRunner()
    {
        require_once 'Lunette/Application.php';
        $runner = $this->object->getScriptRunner(new Lunette_Application());
        $this->assertType('Lunette_Package_ScriptRunner', $runner);
    }
    
    /**
     * Tests the 'getState' method
     */
    public function testGetState()
    {
        require_once 'Lunette/Package/State.php';
        require_once 'Lunette/Package/Service.php';
        $service = new Lunette_Package_Service(Xyster_Orm::getInstance());
        $this->assertSame(Lunette_Package_State::Installed(), $this->object->getState($service));
    }

    /**
     * Tests the 'toString' method
     */
    public function test__toString()
    {
        $this->assertEquals('foobar-package 2.0.2-lunette5', $this->object->__toString());
    }
}

