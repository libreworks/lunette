if (!defined('PHPUnit_MAIN_METHOD')) {
    define('PHPUnit_MAIN_METHOD', 'Lunette_File_ReaderTest::main');
}
require_once dirname(dirname(dirname(dirname(__FILE__)))) . DIRECTORY_SEPARATOR . 'TestHelper.php';
require_once 'Lunette/File/Reader.php';

/**
 * Test class for Lunette_File_Reader.
 * Generated by PHPUnit on 2008-06-01 at 22:34:13.
 */
class Lunette_File_ReaderTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var    Lunette_File_Reader
     */
    protected $object;

    protected $file;
    
    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';
        $suite  = new PHPUnit_Framework_TestSuite('Lunette_File_ReaderTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
        $this->file = dirname(__FILE__) . '/_files/alicecp1.txt';
        $this->object = new Lunette_File_ReaderImpl($this->file);
    }

    /**
     * Tears down the fixture
     */
    protected function tearDown()
    {
        $this->object = null;
    }

    /**
     * Tests creating a reader for a file not there
     */
    public function testFileNotFound()
    {
        $this->setExpectedException('Lunette_File_Exception');
        $object = new Lunette_File_ReaderImpl('./nothinghere');
    }
    
    /**
     * Tests the 'getFilename' method
     */
    public function testGetFilename()
    {
        $this->assertEquals($this->file, $this->object->getFilename());
    }
}

class Lunette_File_ReaderImpl extends Lunette_File_Reader
{
}

// Call Lunette_File_ReaderTest::main() if this source file is executed directly.
if (PHPUnit_MAIN_METHOD == 'Lunette_File_ReaderTest::main') {
    Lunette_File_ReaderTest::main();
}
