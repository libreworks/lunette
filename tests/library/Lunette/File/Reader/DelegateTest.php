/**
 * Test class for Lunette_File_Reader_Delegate.
 * Generated by PHPUnit on 2008-06-01 at 23:33:09.
 */
class Lunette_File_Reader_DelegateTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var    Lunette_File_Reader_Delegate
     */
    protected $object;
    
    /**
     * @var    Lunette_File_Reader_File
     */
    protected $delegate;

    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';
        $suite  = new PHPUnit_Framework_TestSuite('Lunette_File_Reader_DelegateTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
        $this->delegate = new Lunette_File_Reader_File(dirname(dirname(__FILE__)) . '/_files/alicecp1.txt');
        $this->object = new Lunette_File_Reader_DelegateImpl($this->delegate);
    }

    /**
     * Tears down the fixture
     */
    protected function tearDown()
    {
        $this->object = null;
    }

    /**
     * Tests the 'getDelegate' method
     */
    public function testGetDelegate()
    {
        $this->assertSame($this->delegate, $this->object->getDelegate());
    }

    /**
     * Tests the 'getFilename' method
     */
    public function testGetFilename()
    {
        $this->assertSame($this->delegate->getFilename(), $this->object->getFilename());
    }
}

class Lunette_File_Reader_DelegateImpl extends Lunette_File_Reader_Delegate
{
}

