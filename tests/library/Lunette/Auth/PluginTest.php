// Call Lunette_Auth_PluginTest::main() if this source file is executed directly.
if (!defined('PHPUnit_MAIN_METHOD')) {
    define('PHPUnit_MAIN_METHOD', 'Lunette_Auth_PluginTest::main');
}
require_once dirname(dirname(dirname(dirname(__FILE__)))) . DIRECTORY_SEPARATOR . 'TestHelper.php';
require_once 'Lunette/Auth/Plugin.php';
require_once 'Lunette/Auth/Adapter.php';
require_once 'Lunette/User/Service.php';
require_once 'Lunette/TestDbSetup.php';

/**
 * Test class for Lunette_Auth_Plugin.
 * Generated by PHPUnit on 2008-06-30 at 16:55:55.
 */
class Lunette_Auth_PluginTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var    Lunette_Auth_Plugin
     */
    protected $object;
    /**
     * @var Lunette_User_Service
     */
    protected $service;
    
    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';
        $suite  = new PHPUnit_Framework_TestSuite('Lunette_Auth_PluginTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
        $this->service = new Lunette_User_Service(Xyster_Orm::getInstance());
        $this->object = new Lunette_Auth_Plugin($this->service);
    }

    /**
     * Tests the 'canChangePassword' method
     */
    public function testCanChangePassword()
    {
        $this->assertTrue($this->object->canChangePassword());
    }

    /**
     * Tests the 'getAuthAdapter' method
     */
    public function testGetAuthAdapter()
    {
        $creds = array();
        $adapter = $this->object->getAuthAdapter($creds);
        $this->assertType('Lunette_Auth_Adapter', $adapter);
    }

    /**
     * Tests the 'getForm' method
     */
    public function testGetForm()
    {
        $form = $this->object->getForm();
        $this->assertType('Lunette_Auth_Form', $form);
    }

    /**
     * Tests the 'getUserInfo' method
     */
    public function testGetUserInfo()
    {
        $this->assertEquals(array(), $this->object->getUserInfo('foobar'));
    }

    /**
     * Tests the 'setPassword' method
     */
    public function testSetPassword()
    {
        $this->_setup();
        $username = 'doublecompile';
        $password = 'newpassword';
        $this->object->setPassword($username, $password);
        $user = $this->service->getByUsername('doublecompile');
        $this->assertEquals(md5($password), $user->password);
        $this->_destroy();
    }

    /**
     * Tests the 'supportsAutoAuth' method
     */
    public function testSupportsAutoAuth()
    {
        $this->assertFalse($this->object->supportsAutoAuth());
    }
    
    /**
     * Sets up the database for tests
     */
    protected function _setup()
    {
        $db = new Lunette_TestDbSetup();
        $db->setupUser();
        
        $orm = Xyster_Orm::getInstance();
        $user = new LunetteUser;
        $user->username = 'doublecompile';
        $user->email = 'doublecompile@irrationallogic.net';
        $user->password = md5('foobar');
        $orm->persist($user);
        $orm->commit();
    }
    
    /**
     * Cleans up the test
     */
    protected function _destroy()
    {
        $db = new Lunette_TestDbSetup();
        $db->tearDownUser();
    }
}

// Call Lunette_Auth_PluginTest::main() if this source file is executed directly.
if (PHPUnit_MAIN_METHOD == 'Lunette_Auth_PluginTest::main') {
    Lunette_Auth_PluginTest::main();
}
