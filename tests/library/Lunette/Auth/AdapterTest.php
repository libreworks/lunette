if (!defined('PHPUnit_MAIN_METHOD')) {
    define('PHPUnit_MAIN_METHOD', 'Lunette_Auth_AdapterTest::main');
}
require_once dirname(dirname(dirname(dirname(__FILE__)))) . DIRECTORY_SEPARATOR . 'TestHelper.php';
require_once 'Lunette/Auth/Adapter.php';
require_once 'Lunette/User/Service.php';
require_once 'Lunette/TestDbSetup.php';

/**
 * Test class for Lunette_Auth_Adapter.
 * Generated by PHPUnit on 2008-06-30 at 16:19:02.
 */
class Lunette_Auth_AdapterTest extends PHPUnit_Framework_TestCase
{
    protected $service;
    
    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';
        $suite  = new PHPUnit_Framework_TestSuite('Lunette_Auth_AdapterTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
        $db = new Lunette_TestDbSetup();
        $db->setupUser();
        $this->service = new Lunette_User_Service(Xyster_Orm::getInstance());
    }
    
    /**
     * Tears down the fixture
     */
    protected function tearDown()
    {
        $db = new Lunette_TestDbSetup();
        $db->tearDownUser(); 
    }

    /**
     * Tests the get and set operation of the class
     */
    public function testGetAndSet()
    {
        $email = 'doublecompile@irrationallogic.net';
        $password = 'foobar';
        $adapter = new Lunette_Auth_Adapter($this->service, $email, $password);
        
        $this->assertEquals($email, $adapter->getEmail());
        $this->assertEquals($password, $adapter->getPassword());
        
        $return = $adapter->setEmail('lee@sitec-consulting.net');
        $this->assertSame($adapter, $return);
        $this->assertEquals('lee@sitec-consulting.net', $adapter->getEmail());
        $return = $adapter->setPassword('foobar2');
        $this->assertSame($adapter, $return);
        $this->assertEquals('foobar2', $adapter->getPassword());
    }

    /**
     * Tests the 'authenticate' method
     */
    public function testAuthenticate()
    {
        $orm = Xyster_Orm::getInstance();
        $user = new LunetteUser;
        $user->username = 'doublecompile';
        $user->email = 'doublecompile@irrationallogic.net';
        $user->password = md5('foobar');
        $orm->persist($user);
        $orm->commit();
        
        $adapter = new Lunette_Auth_Adapter($this->service, 'doublecompile@irrationallogic.net', 'foobar');
        $result = $adapter->authenticate();
        $this->assertType('Zend_Auth_Result', $result);
        $this->assertEquals(1, $result->getCode());
        $this->assertEquals('doublecompile@irrationallogic.net', $result->getIdentity());
        
        $adapter->setPassword(null);
        $result = $adapter->authenticate();
        $this->assertType('Zend_Auth_Result', $result);
        $this->assertEquals(-3, $result->getCode());
        $this->assertNull($result->getIdentity());
    }
}

// Call Lunette_Auth_AdapterTest::main() if this source file is executed directly.
if (PHPUnit_MAIN_METHOD == 'Lunette_Auth_AdapterTest::main') {
    Lunette_Auth_AdapterTest::main();
}
